lvim.builtin.which_key.mappings["B-"] = { ":split<CR>", "Split" }
lvim.builtin.which_key.mappings["B\\"] = { ":vs<CR>", "Vertical split" }
lvim.builtin.which_key.mappings["Bj"] = { ":resize +20<CR>", "Split resize +" }
lvim.builtin.which_key.mappings["Bk"] = { ":resize -20<CR>", "Split resize -" }
lvim.builtin.which_key.mappings["Bl"] = { ":vertical resize +20<CR>", "Split resize +" }
lvim.builtin.which_key.mappings["Bh"] = { ":vertical resize -20<CR>", "Split resize -" }

lvim.keys.normal_mode["<leader>\\"] = ":vs<CR>"
